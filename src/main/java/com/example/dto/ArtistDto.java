package com.example.dto;

public class ArtistDto {
    private int artistId;
    private String name;

    public ArtistDto() {}

    public int getArtistId() {
        return artistId;
    }

    public void setArtistId(int artistId) {
        this.artistId = artistId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
